// ─── Core Types ───────────────────────────────────────────────────────────────

export type UniversityType =
  | 'research'
  | 'comprehensive'
  | 'technical'
  | 'teacher-training'
  | 'medical'
  | 'language'
  | 'agricultural'
  | 'vocational'

export type CostLevel = 'low' | 'medium' | 'high'

export type ApplicationRoute = 'university-portal' | 'csc' | 'cucas'

export type DegreeLevel = 'college' | 'bachelor' | 'master' | 'phd'

export type Ownership = 'public' | 'private'

// ─── University Interface ──────────────────────────────────────────────────────

export interface University {
  id: string
  name: string
  chineseName: string
  city: string
  province: string
  type: UniversityType
  ownership: Ownership

  // Rankings
  qsRanking?: number
  nationalRanking?: number // Chinese national rank (Soft Science / ARWU)

  // International student features
  hasInternationalOffice: boolean
  hasEnglishPrograms: boolean
  hasChineseProgramsForForeigners: boolean
  hasCscScholarship: boolean // CSC Government Scholarship eligible
  hasOnlineApplication: boolean

  // Costs (approximate USD)
  tuitionMin: number // per academic year
  tuitionMax: number
  hostelCostPerYear: number
  livingCostPerMonth: number // estimated city average
  cityCostLevel: CostLevel

  // Application
  applicationRoutes: ApplicationRoute[]
  applicationDeadline: string // e.g. "May 31"
  requiredDocuments: string[]

  // Links
  website: string
  admissionsWebsite?: string

  // Academic profile
  strongPrograms: string[] // e.g. ["Engineering", "AI / Computer Science"]
  description: string

  // International students
  internationalStudents?: number // approximate headcount

  // Degree levels offered to international students
  degreeLevels?: DegreeLevel[] // defaults to all three if omitted
}

// ─── Filter Interface ──────────────────────────────────────────────────────────

export interface UniversityFilters {
  search: string
  province: string            // '' = all
  types: UniversityType[]     // empty = all
  ownership: Ownership | 'all'
  cityCostLevel: CostLevel | 'all'
  minInternationalStudents: number  // 0 = no filter
  degreeLevel: DegreeLevel | 'all'
}

