function getDegreeLevels(u: University): DegreeLevel[] {
  if (u.degreeLevels) return u.degreeLevels

  // Medical and pure research universities don't run pre-university/foundation programmes
  const hasCollege = u.type !== 'medical' && u.type !== 'research'

  // Teacher-training and language institutes: college + bachelor + master (no international PhD)
  if (u.type === 'teacher-training' || u.type === 'language') {
    return ['college', 'bachelor', 'master']
  }

  // Lower-ranked non-research/non-medical universities: college + bachelor + master
  if ((u.nationalRanking ?? 999) > 80 && u.type !== 'research' && u.type !== 'medical') {
    return ['college', 'bachelor', 'master']
  }

  // Top-tier comprehensive, technical, agricultural: all levels including college (预科)
  return hasCollege
    ? ['college', 'bachelor', 'master', 'phd']
    : ['bachelor', 'master', 'phd']
}

const DEGREE_KEYWORDS: { keywords: string[]; level: DegreeLevel }[] = [
  { keywords: ['master', 'masters', 'postgraduate', 'msc', 'mba', 'ma', 'meng'], level: 'master' },
  { keywords: ['bachelor', 'bachelors', 'undergraduate', 'bsc', 'ba', 'beng'], level: 'bachelor' },
  { keywords: ['phd', 'doctorate', 'doctoral', 'dphil', 'doctor'], level: 'phd' },
  { keywords: ['college', 'pre-university', 'foundation', 'preparatory', 'yueke'], level: 'college' },
]

export function filterUniversities(
  universities: University[],
  filters: UniversityFilters
): University[] {
  return universities.filter((u) => {
    if (filters.search) {
      const q = filters.search.toLowerCase().trim()
      const levels = getDegreeLevels(u)

      const degreeLevelMatch = DEGREE_KEYWORDS.some(
        ({ keywords, level }) =>
          keywords.some((k) => q.includes(k)) && levels.includes(level)
      )

      const textMatch =
        u.name.toLowerCase().includes(q) ||
        u.chineseName.includes(q) ||
        u.city.toLowerCase().includes(q) ||
        u.province.toLowerCase().includes(q) ||
        u.description.toLowerCase().includes(q) ||
        u.strongPrograms.some((p) => p.toLowerCase().includes(q))

      if (!textMatch && !degreeLevelMatch) return false
    }

    if (filters.province && u.province !== filters.province) return false
    if (filters.types.length > 0 && !filters.types.includes(u.type)) return false
    if (filters.ownership !== 'all' && u.ownership !== filters.ownership) return false
    if (filters.cityCostLevel !== 'all' && u.cityCostLevel !== filters.cityCostLevel) return false
    if (filters.minInternationalStudents > 0 && (u.internationalStudents ?? 0) < filters.minInternationalStudents) return false
    if (filters.degreeLevel !== 'all' && !getDegreeLevels(u).includes(filters.degreeLevel)) return false
    return true
  })
}

export function sortUniversities(universities: University[], order: SortOrder): University[] {
  return [...universities].sort((a, b) => {
    switch (order) {
      case 'qs-rank':
        return (a.qsRanking ?? 9999) - (b.qsRanking ?? 9999)
      case 'national-rank':
        return (a.nationalRanking ?? 9999) - (b.nationalRanking ?? 9999)
      case 'name':
        return a.name.localeCompare(b.name)
      case 'cost-low':
        return a.tuitionMin - b.tuitionMin
      case 'cost-high':
        return b.tuitionMax - a.tuitionMax
      default:
        return (a.qsRanking ?? 9999) - (b.qsRanking ?? 9999)
    }
  })
}
